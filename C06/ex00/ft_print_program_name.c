#include <unistd.h>

int	main(int argc, char **argv)
{
	int		len;

	len = 0;
	while (argv[0][len] != '\0' && argc == 1)
	{
		write(1, argv[0] + len, 1);
		len++;
	}
	write(1, "\n", 1);
}
